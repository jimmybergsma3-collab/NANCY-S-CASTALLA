create or replace function create_order_with_inventory(
  p_order_id text,
  p_customer_name text,
  p_customer_email text,
  p_customer_phone text,
  p_fulfillment text,
  p_notes text,
  p_total numeric,
  p_lines jsonb
)
returns table(order_number bigint)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_customer_id uuid;
  v_order_number bigint;
  v_line jsonb;
  v_stock_delta numeric;
begin
  insert into customers (name, email, phone)
  values (p_customer_name, lower(p_customer_email), coalesce(p_customer_phone, ''))
  on conflict (email) do update
    set name = excluded.name,
        phone = case when excluded.phone <> '' then excluded.phone else customers.phone end,
        updated_at = now()
  returning id into v_customer_id;

  insert into orders (
    id, customer_id, customer_name, customer_email, customer_phone,
    fulfillment, delivery_method, notes, total, status, payment_status
  ) values (
    p_order_id, v_customer_id, p_customer_name, lower(p_customer_email), coalesce(p_customer_phone, ''),
    p_fulfillment, p_fulfillment, coalesce(p_notes, ''), p_total, 'new', 'pending'
  ) returning orders.order_number into v_order_number;

  for v_line in select value from jsonb_array_elements(p_lines)
  loop
    insert into order_items (
      order_id, product_id, product_name, quantity, unit,
      package_label, package_quantity, sale_price_incl_vat
    ) values (
      p_order_id,
      v_line->>'productId',
      v_line->>'name',
      (v_line->>'quantity')::integer,
      v_line->>'unit',
      coalesce(v_line->>'packageLabel', ''),
      coalesce((v_line->>'packageQuantity')::numeric, 1),
      (v_line->>'salePriceInclVat')::numeric
    );

    v_stock_delta := (v_line->>'quantity')::numeric * coalesce((v_line->>'packageQuantity')::numeric, 1);
    update products
      set stock_quantity = greatest(stock_quantity - v_stock_delta, 0)
      where id = v_line->>'productId' and track_inventory = true;

    if found then
      insert into inventory_movements (product_id, order_id, movement_type, quantity, reference)
      values (v_line->>'productId', p_order_id, 'sale', -v_stock_delta, 'Order ' || v_order_number);
    end if;
  end loop;

  return query select v_order_number;
end;
$$;
