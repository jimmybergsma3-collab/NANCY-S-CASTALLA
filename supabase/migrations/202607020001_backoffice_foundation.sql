create table if not exists customers (
  id uuid primary key default gen_random_uuid(),
  auth_user_id uuid unique references auth.users(id) on delete set null,
  name text not null,
  email text not null unique,
  phone text not null default '',
  address text not null default '',
  language text not null default 'en',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

do $$
begin
  if not exists (select 1 from pg_constraint where conname = 'orders_customer_id_fkey') then
    alter table orders
      add constraint orders_customer_id_fkey
      foreign key (customer_id) references customers(id) on delete set null
      not valid;
  end if;
end $$;

create table if not exists suppliers (
  id uuid primary key default gen_random_uuid(),
  code text unique,
  name text not null unique,
  email text not null default '',
  phone text not null default '',
  address text not null default '',
  active boolean not null default true,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

insert into suppliers (name)
select distinct supplier from products where supplier <> ''
on conflict (name) do nothing;

create table if not exists inventory_movements (
  id uuid primary key default gen_random_uuid(),
  product_id text references products(id) on delete set null,
  order_id text references orders(id) on delete set null,
  movement_type text not null,
  quantity numeric not null,
  reference text not null default '',
  notes text not null default '',
  created_at timestamptz not null default now()
);
create index if not exists inventory_movements_product_idx on inventory_movements(product_id, created_at desc);

